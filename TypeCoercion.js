// Type Coercion
// Coercion is the process of converting a value from one type to another (such as string to number, object to boolean, and so on).

let a = 1 + "2";
let b = 10 - "3";
let c = true + 1;
let d = 1 + 2 + "3";
let e = "3" * 3;

console.log(a); // 12
console.log(b); // 7
console.log(c); // 1
console.log(d); // 33
console.log(e); // 9
